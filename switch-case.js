let day = "Monday";

switch (day) 
{
    case "Monday":
        console.log(" Its Monday");
        break;

    case "Tuesday":
        console.log(" Its Tuesday");
        break;

    case "Wednesday":
        console.log(" Its Wednesday");
        break;

    case "Thursday":
        console.log(" Its Thursday");
        break;

    case "Friday":
        console.log(" Its Friday");
        break;

    case "Saturday":
        console.log(" Its Saturday");
        break;

    default:
        console.log("Nothing");
        break;
}